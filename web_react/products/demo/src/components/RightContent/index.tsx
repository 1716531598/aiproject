export const SelectTheme = ({ onThemeChange = () => {} }) => {
  const items = [
    {
      label: '盛邦主题',
      key: 'webray',
    },
    {
      label: '深色主题',
      key: 'dark',
    },
    {
      label: '浅色主题',
      key: 'light',
    },
  ];

  // 从initialState获取当前主题
  const { initialState } = useModel('@@initialState');

  // 在组件加载时，优先从 localStorage 获取存储的主题
  useEffect(() => {
    const savedTheme = localStorage.getItem('app-theme');
    if (savedTheme) {
      onThemeChange(savedTheme);
    }
  }, []);

  return (
    <Dropdown
      menu={{
        items,
        selectable: true,
        selectedKeys: [
          localStorage.getItem('app-theme') ||
            initialState?.settings?.currentTheme ||
            DEFAULT_THEME,
        ],
        onClick: (e) => {
          localStorage.setItem('app-theme', e.key);
          onThemeChange(e.key);
          const event = new CustomEvent('app-theme', {
            detail: { theme: e.key },
          });
          window.dispatchEvent(event);
          document.documentElement.setAttribute('data-theme', e.key);
          ConfigProvider.config({
            theme: {
              primaryColor: THEME[e.key].colorPrimary,
            },
          });
        },
      }}
    >
      <SunOutlined style={{ marginRight: 16 }} />
    </Dropdown>
  );
};

