/**上传文件 大小 后缀限制
 * file:文件
 * accept：后缀
 * fileSize：文件大小
 * fileName:文件名
 * */
export function uploadFileSizeAccept(params) {
  const { file, accept, fileSize, fileName } = params;
  const isLtM = fileSize ? file.size / 1024 / 1024 < fileSize : true;
  const r = new RegExp(`(${accept?.replace(/,/g, '|')})$`);
  const sameName = fileName
    ? fileName === file?.name?.replace(/\.[^/.]+$/, '')
    : true;
  if ((!accept || r.test(file.name)) && isLtM && sameName) {
    return true;
  } else {
    if (fileSize && !isLtM) {
      message['error'](
        formatMessage(
          { id: 'validator.file size max' },
          { max: fileSize + 'M' },
        ),
      );
    } else if (!sameName) {
      message['error'](formatMessage({ id: 'File name error' }));
    } else {
      message['error'](formatMessage({ id: 'File type error' }));
    }
  }
  return false;
}

