export function isUTF8(bytes) {
  let i = 0;
  while (i < bytes.length) {
    if (
      // ASCII
      bytes[i] === 0x09 ||
      bytes[i] === 0x0a ||
      bytes[i] === 0x0d ||
      (bytes[i] >= 0x20 && bytes[i] <= 0x7e)
    ) {
      i += 1;
      continue;
    }

    if (
      // non-overlong 2-byte
      bytes[i] >= 0xc2 &&
      bytes[i] <= 0xdf &&
      bytes[i + 1] >= 0x80 &&
      bytes[i + 1] <= 0xbf
    ) {
      i += 2;
      continue;
    }

    if (
      // excluding overlongs
      (bytes[i] === 0xe0 &&
        bytes[i + 1] >= 0xa0 &&
        bytes[i + 1] <= 0xbf &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf) || // straight 3-byte
      (((bytes[i] >= 0xe1 && bytes[i] <= 0xec) ||
        bytes[i] === 0xee ||
        bytes[i] === 0xef) &&
        bytes[i + 1] >= 0x80 &&
        bytes[i + 1] <= 0xbf &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf) || // excluding surrogates
      (bytes[i] === 0xed &&
        bytes[i + 1] >= 0x80 &&
        bytes[i + 1] <= 0x9f &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf)
    ) {
      i += 3;
      continue;
    }

    if (
      // planes 1-3
      (bytes[i] === 0xf0 &&
        bytes[i + 1] >= 0x90 &&
        bytes[i + 1] <= 0xbf &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf &&
        bytes[i + 3] >= 0x80 &&
        bytes[i + 3] <= 0xbf) || // planes 4-15
      (bytes[i] >= 0xf1 &&
        bytes[i] <= 0xf3 &&
        bytes[i + 1] >= 0x80 &&
        bytes[i + 1] <= 0xbf &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf &&
        bytes[i + 3] >= 0x80 &&
        bytes[i + 3] <= 0xbf) || // plane 16
      (bytes[i] === 0xf4 &&
        bytes[i + 1] >= 0x80 &&
        bytes[i + 1] <= 0x8f &&
        bytes[i + 2] >= 0x80 &&
        bytes[i + 2] <= 0xbf &&
        bytes[i + 3] >= 0x80 &&
        bytes[i + 3] <= 0xbf)
    ) {
      i += 4;
      continue;
    }
    return false;
  }
  return true;
}

export function renderOptions(item, empty = true, id = false) {
  const option = [] as any;
  if (empty) {
    option.push(
      <Option key={0} value={0}>
        <FormattedMessage id="empty" />
      </Option>,
    );
  }
  if (item && Array.isArray(item)) {
    //数组
    if (!id) {
      item.forEach((value) => {
        option.push(
          <Option
            value={Number(Object.keys(value)[0])}
            key={Object.values(value)[0]}
          >
            {Object.values(value)[0]}
          </Option>,
        );
      });
    } else {
      item.forEach((value) => {
        option.push(
          <Option value={Number(value.id)} key={Number(value.id)}>
            {value.name}
          </Option>,
        );
      });
    }
  } else {
    Object.keys(item).forEach((key) => {
      //json
      option.push(
        <Option value={Number(key)} key={item[key]}>
          {item[key]}
        </Option>,
      );
    });
  }
  return option;
}

export function translate_label(data) {
  if (data && Array.isArray(data)) {
    data.forEach((value, index) => {
      if (Object.prototype.toString.call(value) === '[object Object]') {
        if (value.label) {
          value.label = formatMessage({ id: value.label });
        }
      } else {
        data[index] = formatMessage({ id: value });
      }
    });
  } else {
    Object.keys(data).forEach((key) => {
      data[key] = formatMessage({ id: data[key] });
    });
  }
  return data;
}

export function renderInnerHTML(str, returnNull = false) {
  // returnNull 为真时返回空字符串
  let _str = str;
  if (_str && typeof _str === 'string') {
    _str = str
      .replace(/\</g, '&lt;')
      .replace(/\>/g, '&gt;')
      .replace(/\\r\\n|\r\n|\n/g, '<br/>');
    return <span dangerouslySetInnerHTML={{ __html: _str }}></span>;
  } else {
    if (returnNull) {
      return '';
    } else {
      return <FormattedMessage id="unknown" />;
    }
  }
}

export function renderInfo(str, returnNull = false, returnText = false) {
  // returnNull 为真时返回空字符串
  if (!str) {
    if (returnNull) {
      return '';
    } else if (returnText) {
      return <FormattedMessage id="unknown" />;
    } else {
      return '-';
    }
  } else {
    return str;
  }
}

//严重级别区分显示翻译信息
export function severityFormatMessage(text, type = 0) {
  const { formatMessage } = getIntl();
  let LOW = '';
  let MEDIUM = '';
  let HIGH = '';
  if (type === 1) {
    LOW = formatMessage({ id: 'Low_' });
    MEDIUM = formatMessage({ id: 'Middle_' });
    HIGH = formatMessage({ id: 'High_' });
  } else if (type === 2) {
    LOW = formatMessage({ id: 'Low' });
    MEDIUM = formatMessage({ id: 'Middle' });
    HIGH = formatMessage({ id: 'High' });
  } else {
    LOW = formatMessage({ id: 'LOW_' });
    MEDIUM = formatMessage({ id: 'MEDIUM_' });
    HIGH = formatMessage({ id: 'HIGH_' });
  }

  if (text === 'Low' || text === 0) return LOW;
  else if (text === 'Middle' || text === 1) return MEDIUM;
  else if (text === 'High' || text === 2) return HIGH;
  else return text;
}

